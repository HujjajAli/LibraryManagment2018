package com.lms.model;

public class Address {
	
	private int id;
	private String country;
	private String province;
	private String city;
	private String street_area_houseNo;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getStreet_area_houseNo() {
		return street_area_houseNo;
	}
	public void setStreet_area_houseNo(String street_area_houseNo) {
		this.street_area_houseNo = street_area_houseNo;
	}
	
}
